    public double calcNrb(double bw) {
        double nrb = 0;
        if (bw == 1.4) {
            nrb = 6;
        } else if (bw == 3.0) {
            nrb = 15;
        } else if (bw == 5.0) {
            nrb = 25;
        } else if (bw == 10.0) {
            nrb = 50;
        } else if (bw == 15.0) {
            nrb = 75;
        } else if (bw == 20.0) {
            nrb = 100;
        }
        return nrb;
    }

    public double calcNt(double bw, double temp) {
        double K = 1.38 * 10 * Math.pow(10, -23);
        double wrb = 180 * 1000;
        double nrb = calcNrb(bw);
        return 10 * Math.log10(K * temp * wrb * nrb);

    }

    public double calcMAPL(double EIRP, double Rsensitive, double losses, double SystemGain, double Rxlosses) {
        return EIRP - Rsensitive - losses + SystemGain - Rxlosses + 20;

    }

