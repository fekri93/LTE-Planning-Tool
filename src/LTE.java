    private double calcSessionTrafficForEachService(double bearer, double sessionTime, double sessionDuty, double bler) {
        return bearer * sessionTime * sessionDuty / (1 - bler);
    }

    private double calcThroughputForService(double bearer, double sessionTime, double sessionDuty, double bler, double bhsa, double servicePent) {
        return calcSessionTrafficForEachService(bearer, sessionTime, sessionDuty, bler) * bhsa * servicePent;
    }

    private double calcSessionTrafficPerUser(double _voip, double _vidoPhone, double _vidoConf, double _gaming, double _streaming, double _message, double _browsing, double _email) {

        return (_voip + _vidoPhone + _vidoConf + _gaming + _streaming + _message + _browsing + _email) / 3600;
    }

    private double calcTotalTraffic(int activeUserNum, double SessionTrafficPerUser) {
        return activeUserNum * SessionTrafficPerUser;
    }

    private double calcResourcePerBlock(String cyclePrefix) {
        if (cyclePrefix.equals("Long cp")) {
            return 84;
        } else {
            return 72;
        }
    }

    private int calcModulationOrder(String modulationType) {
        if (modulationType.equals("QPSK")) {
            return 2;
        } else if (modulationType.equals("16 QUAM")) {
            return 4;
        } else {
            return 6;
        }
    }

    private double calcCode(String modulationType) {
        if (modulationType.equals("QPSK")) {
            return 0.33;
        } else if (modulationType.equals("16 QUAM")) {
            return 0.75;
        } else {
            return 0.93;
        }
    }

    private double calcTcell(double bw, String cyclePrefix, String modulationType, double cch) {
        return calcNrb(bw) * calcResourcePerBlock(cyclePrefix) * calcModulationOrder(modulationType) * calcCode(modulationType) * (1 - cch) * 2000;
    }

    private int calcNumOfCellPerSite(String anntenaType) {

        if (anntenaType.equals("Omni")) {
            return 1;
        } else if (anntenaType.equals("Bi-sector")) {
            return 2;
        } else if (anntenaType.equals("Tri-sector")) {
            return 3;
        } else {
            return 6;
        }
    }

    private double calcTsite(double tCell, String anntenaType, double SystemLoad) {
        return (tCell * calcNumOfCellPerSite(anntenaType) * SystemLoad) / 1024;
    }

    private int calcNumOfSitesCapacity(double totalTraffic, double Tsite) {
        return (int) Math.ceil(totalTraffic / Tsite);
    }

